package control_statements;

public class ForLoop {

	public static void main(String[] args) {
		// we can write three condition in one line
		//i.e initialization;condition;increament
		//When we knows starting and ending point then we go for for loop
		
		for(int i=0;i<5;i++) {
			System.out.println("Hello");
		}

	}

}
